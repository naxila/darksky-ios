import Foundation
import UIKit

extension UIColor {
    
    //MARK: - Defined Colors
    
    struct WeatherCell {
        
        static func light() -> UIColor {
            return UIColor.white
        }
        
        static func dark() -> UIColor {
            return UIColor.darkGray
        }
        
    }
    
}
